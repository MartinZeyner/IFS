package IFS;

public class Parameters
{
    private float a, b, c, d, e, f, p;

    public Parameters(float a, float b, float c, float d, float e, float f, float p)
    {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
        this.e = e;
        this.f = f;
        this.p = p;
    }

    public float getA()
    {
        return a;
    }

    public float getB()
    {
        return b;
    }

    public float getC()
    {
        return c;
    }

    public float getD()
    {
        return d;
    }

    public float getE()
    {
        return e;
    }

    public float getF()
    {
        return f;
    }

    public float getP()
    {
        return p;
    }
}
